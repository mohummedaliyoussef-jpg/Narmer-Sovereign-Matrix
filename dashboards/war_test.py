import numpy as np

before = {
    "الحوكمة والامتثال": 70, "القوة العسكرية": 80, "المناعة السيبرانية": 75,
    "البنية التحتية الرقمية": 65, "سلاسل الإمداد": 65, "رأس المال البشري": 70,
    "السيادة المالية": 60, "استقلالية الطاقة": 70, "الأمن الغذائي": 55,
    "الابتكار": 55, "الدبلوماسية": 65, "الاستقرار الاجتماعي": 60,
    "الاستدامة البيئية": 50, "السيادة الفضائية": 40, "سيادة الذكاء الاصطناعي": 50,
    "القوة الناعمة": 55, "الأمن الصحي": 65
}

shocks = {
    "المناعة السيبرانية": -45, "القوة العسكرية": -15, "استقلالية الطاقة": -50,
    "سلاسل الإمداد": -50, "السيادة المالية": -35, "الأمن الغذائي": -30,
    "الاستقرار الاجتماعي": -40, "البنية التحتية الرقمية": -35,
    "رأس المال البشري": -20, "الدبلوماسية": -50, "الاستدامة البيئية": -35,
    "الحوكمة والامتثال": -40, "الأمن الصحي": -45, "الابتكار": -30,
    "السيادة الفضائية": -20, "سيادة الذكاء الاصطناعي": -20, "القوة الناعمة": -45
}

after = before.copy()
for dim, shock in shocks.items():
    after[dim] = max(0, min(100, before[dim] + shock))

weights = np.array([0.085,0.11,0.13,0.09,0.08,0.07,0.085,0.10,0.06,0.065,0.05,0.055,0.04,0.035,0.06,0.03,0.045])
weights /= weights.sum()

def v_score_simple(inputs, dims):
    vals = np.clip([inputs[d]/100.0 for d in dims], 0.01, 1.0)
    return float(np.exp(np.sum(weights * np.log(vals))) * 100)

dims = list(before.keys())
v_before = v_score_simple(before, dims)
v_after = v_score_simple(after, dims)

print("=" * 60)
print("🔱 اختبار مصفوفة نارمر – سيناريو الحرب الكبرى")
print("=" * 60)
print(f"\n📊 V‑Score قبل الحرب: {v_before:.2f}%")
print(f"📊 V‑Score بعد الحرب : {v_after:.2f}%")
print(f"📉 الانهيار الكلي    : {v_before - v_after:.2f} نقطة ({(v_before - v_after)/v_before*100:.1f}%)")
print("\n🔴 أخطر 5 أبعاد متأثرة:")
worst = sorted(shocks.items(), key=lambda x: x[1])[:5]
for dim, shock in worst:
    print(f"  • {dim}: {before[dim]}% → {after[dim]}% ({shock:+d}%)")